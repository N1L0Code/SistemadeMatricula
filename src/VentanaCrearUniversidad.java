    private void jbtnguardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnguardarActionPerformed
        // TODO add your handling code here:
        String ruc;
        String denominacion;
        Universidad universidad;
        Reporte r=ventanaPrincipal.getReporte();
        if(txtruc.getText().length()!=0){ 
            ruc = String.valueOf(txtruc.getText());
            universidad = r.buscarUniversidad(ruc);
             if(universidad == null){
             denominacion = txtdenominacion.getText();
             universidad = new Universidad(denominacion, ruc);                                  
             r.agregarUniversidad(universidad);
             JOptionPane.showMessageDialog(this, "Se registro los datos");
            txtruc.setText("");
            txtdenominacion.setText("");
            
            universidad.setFacultad(new Facultad("DERECHO")); 
            universidad.setFacultad(new Facultad("CIENCIAS FISICAS Y MATEMATICAS"));
            universidad.setFacultad(new Facultad("CIENCIAS SOCIALES"));
            universidad.setFacultad(new Facultad("CIENCIAS ECONOMICAS"));
            this.ventanaPrincipal.AgregarUniversidad(universidad);
        }else{
            JOptionPane.showMessageDialog(this, "El ruc ya esta registrado");
            txtruc.setText("");
        }
        }
    }//GEN-LAST:event_jbtnguardarActionPerformed

