    private void btnguardar2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnguardar2ActionPerformed
      
        String ruc;
        String codigo;
        String nombre;
        String celular;
        String nombrefacultad;
        Universidad universidad;
        Facultad facultad;
        boolean encontrado=false;
        
    if(txtruc.getText().length()!=0){    
        ruc = String.valueOf(txtruc.getText());
        Iterator<Universidad> iterador = uni.iterator();
        while(iterador.hasNext() && encontrado == false){
            universidad = iterador.next();
            if(ruc.equals(universidad.getRuc())){
                encontrado = true;
                codigo= txtcodigo.getText();
                nombre=txtnombre.getText();
                celular=txtcelular.getText();
                nombrefacultad=txtfacultad.getText();
                Alumno al=new Alumno(nombre,codigo,celular, universidad);
                //Facultad fa=new Facultad(nombrefacultad);
                System.out.println(al.getNombre());
                universidad.matricular(al);
                facultad=universidad.buscarFacultad(nombrefacultad);
                if(facultad!=null){
                    universidad.matricular(al);
                    al.setFacultad(facultad);
                }
                JOptionPane.showMessageDialog(this, "Se registro los datos");
            txtruc.setText("");
            txtnombre.setText("");
            txtcelular.setText("");
            txtcodigo.setText("");
            txtfacultad.setText("");
                //System.out.println(universidad.obtenerDatosDelAlumno());
            }
        
        }
    }
            else{
                JOptionPane.showMessageDialog(this, "Ingrese RUC");
            }
                  
    }//GEN-LAST:event_btnguardar2ActionPerformed

