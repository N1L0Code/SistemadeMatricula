    public void mostrarDatos(){
         Universidad uni;
        Object[] datos = new Object[2];
        Reporte r = ventanaPrincipal.getReporte();
        Iterator<Universidad> iterator = r.getIterator();
        
        DefaultTableModel modelo = new DefaultTableModel();
        
        modelo.addColumn("RUC");
        modelo.addColumn("Denominaci√≥n");
        
        while(iterator.hasNext()){
            uni = iterator.next();
            datos[0] = uni.getRuc();
            datos[1] = uni.getDenominacion();
            modelo.addRow(datos);
        }
        
        tblUniversidades.setModel(modelo);
    }

